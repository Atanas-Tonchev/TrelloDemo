package common;

public class AppConfig {
  private String ApiKey;
  private String AuthToken;

  public String getApiKey() {
    return ApiKey;
  }

  public void setApiKey(String apiKey) {
    ApiKey = apiKey;
  }

  public String getAuthToken() {
    return AuthToken;
  }

  public void setAuthToken(String authToken) {
    AuthToken = authToken;
  }

}
